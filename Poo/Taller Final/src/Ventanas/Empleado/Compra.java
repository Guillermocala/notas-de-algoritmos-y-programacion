   private void jButtonCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCompraActionPerformed
      if (jTextFieldNombre.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "El campo de nombre está vacio");
      }
      else if (Integer.parseInt(jFormattedTextFieldValor.getText()) == 0) {
         JOptionPane.showMessageDialog(null, "El campo de valor es invalido");
      }
      else if (jFormattedTextFieldFecha.getText().replaceAll("[ ,/]", "").length() != 8) {
         JOptionPane.showMessageDialog(null, "El campo de fecha está incompleto");
      }
      else if (jFormattedTextFieldCantidad.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "El campo de cantidad está vacio");
      }
      else{
         if (datos.verificaCompra(jTextFieldNombre.getText(), Integer.parseInt(jFormattedTextFieldValor.getText()), 
                 jFormattedTextFieldFecha.getText(), Integer.parseInt(jFormattedTextFieldCantidad.getText()))) {
            limpiarFormulario();
            JOptionPane.showMessageDialog(null, "Articulo Comprado!");
         }
      }
   }//GEN-LAST:event_jButtonCompraActionPerformed

